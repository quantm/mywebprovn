</div>
<div class="clr"></div>
<i class="fa fa-arrow-right"></i> Although the  Beowuff manuscript was written  in about <sub>A.D.</sub> 1000,  it was not 
covered  until  the  seventeenth  century <input type="radio" name="reading_12" title='Click to do the question 12' class="checkbox_model_01_reading_01" id="reading_12_2" value="2"/>. Scholars  do  not know whether 
Beowuff is the sole surviving epic from a flourishing Anglo-Saxon literary period that produced other great epics or whether 
it was <span class="hightlight_word">unique</span>  even in its own  time.<input type="radio" name="reading_12" id="reading_12_3" value="3" title='Click to do the question 12' class="checkbox_model_01_reading_01"/> Many scholars  think that the epic was  probably written sometime  between the late seventh century and  the early ninth century. If they are correct, the original manuscript was probably lost during the ninth-century Viking invasions of Anglia,  in which  the Danes destroyed the Anglo-Saxon monasteries and  their great libraries. However,  other scholars think that the poet's  favorable attitude toward  the Danes must  place the epic's composition after the Viking  invasions and at the  start  of  the  eleventh  century,  when  this  Beowulf manuscript was written.</br> 
<div class="clr"></div>
<i class="fa fa-arrow-right"></i> The identity of the  Beowulf  poet is also uncertain.<input type="radio" name="reading_12" id="reading_12_4" value="4" title='Click to do the question 12' class="checkbox_model_01_reading_01"/> He apparently was a 
Christian who loved the pagan heroic tradition of his ancestors and blended the values of the pagan hero with  
the Christian values of his own country and time. Because he wrote  in  the Anglian  dialect, he probably was either  a monk  in  a 
monastery or  a  poet  in  an  Anglo-Saxon  court  located  north  of  the Thames River</br> 
<div class="clr"></div>
<h4 style="text-align:left">Appeal and Value </h4>
<div class="clr"></div>
Beowuff interests  contemporary  readers  for many  reasons.  First,  it  is  an 
standing adventure  story.  Grendel, Grendel 's mother,  and  the  dragon are marvelous  characters, and each  fight  is unique,  action-packed,  and  exciting. Second, Beowulf is a very appealing hero. He is the perfect warrior, combining extraordinary strength, skill, courage, and loyalty. Like Hercules, he devotes his life to making  the world a safer place. He chooses to risk death in order to help orther people, and he faces his inevitable death with heroism and dignity. Third, the  Beowuff  poet is interested in  the psychological aspects of human behavior.For example, the Danish  hero's welcoming  speech  illustrates  his  jealousy  of Beowuff.  The behavior  of Beowulf's warriors  in  the  dragon  fight  reveals  their cowardice. Beowutl's attitudes  toward heroism reflect  his maturity and  experience, while  King  Hrothgar's  attitudes  toward  life Show  the experiences  of an aged nobleman. 
<div class="clr"></div>
Finally,  the  Beowulf poet <span class='hightlight_word'>exhibits</span>  a  mature  appreciation of  the  transitory
nature  of human  life  and achievement. In  Beowulf,  as  in  the major epics of  other cultures,  the hero  must  create a meaningful  life  in  a world  that is often dangerous and uncaring. He must accept the inevitability of death. He chooses to <span class="hightlight_word">reject</span> despair; instead, he takes pride in himself and In his accomplishments and he values human  relationships. 
