<h3>Appeal and Value </h3>
<div class="clr"></div>
Beowuff interests  contemporary  readers  for many  reasons.  First,  it  is  an 
standing adventure  story.  Grendel, Grendel 's mother,  and  the  dragon are marvelous  characters, and each  fight  is unique,  action-packed,  and  exciting. Second, Beowulf is a very appealing hero. He is the perfect warrior, combining extraordinary strength, skill, courage, and loyalty. Like Hercules, he devotes his life to making  the world a safer place. He chooses to risk death in order to help orther people, and he faces his inevitable death with heroism and dignity. Third, the  Beowuff  poet is interested in  the psychological aspects of human behavior.For example, the Danish  hero's welcoming  speech  illustrates  his  jealousy  of Beowuff.  The behavior  of Beowulf's warriors  in  the  dragon  fight  reveals  their cowardice. Beowutl's attitudes  toward heroism reflect  his maturity and  experience, while  King  Hrothgar's  attitudes  toward  life Show  the experiences  of an aged nobleman. 
<div class="clr"></div>
Finally,  the  Beowulf poet <span class='hightlight_word'>exhibits</span>  a  mature  appreciation of  the  transitory
nature  of human  life  and achievement. In  Beowulf,  as  in  the major epics of  other cultures,  the hero  must  create a meaningful  life  in  a world  that is often dangerous and uncaring. He must accept the inevitability of death. He chooses to <span class="hightlight_word">reject</span> despair; instead, he takes pride in himself and In his accomplishments and he values human  relationships. 
