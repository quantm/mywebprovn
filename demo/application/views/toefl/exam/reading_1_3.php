<h3>Historical Background<br> </h3>
	<i class="fa fa-arrow-right"></i> The epic poem <span id="Beowulf" href="#" target="new">Beowulf</span>, written  in Old English, is the earliest existing Germanic  epic and  one of  four  surviving  Anglo-Saxon manuscripts.  Although 
Beowuff was written  by  an  anonymous  Englishman  in  Old  English,  the  tale 
takes place in that part of Scandinavia from <span class="hightlight_word">which</span> Germanic  tribes emigrated to 
England.  Beowulf comes from Geattand,  the southeastern part of what is  now 
Sweden. Hrothgar, king of  the Danes,  lives near what is now Lelre, on Zealand, 
Denmark's  largest  island.  The  Beowulf epic contains  three major tales about 
Beowulf and  several minor tales  that  reflect  a  rich Germanic oral  tradition  of 
myths, legends, and  folklore.</br>
<div class="clr"></div>
<i class="fa fa-arrow-right"></i> The Beowulf warriors have a  foot  in  both  the Bronze and  Iron Ages.  Their
mead-halls  reflect  the wealthy  living of  the  Bronze Age Northmen,  and  their 
wooden  shields, wood-shafted  spears, and  bronze-hilted  swords are those of 
the Bronze Age warrior.  However,  they carry iron-tipped spears, and  their best 
swords have  iron or  iron-edged blades. Beowulf also orders an  iron shield for 
his fight with a dragon.  Iron replaced bronze because it produced a blade with 
a  cutting  edge  that was  stronger and  sharper.  The Northmen learned how  to 
forge iron  in about 500 B.C.  Although  they had been superior to the European 
Celts  in bronze work, it was the Celts who taught them how to make and design 
iron work.  Iron was accessible everywhere in Scandinavia, usually in the  form 
of <span id='keyword_model_01_question_03'>"bog-iron"</span> found in the layers of peat in peat bogs.
<div class="clr"></div>
<div style="text-align:justify">
The Beowuff epic also reveals interesting aspects of  the lives of the Anglo-Saxons who lived in  England at the time of the anonymous Beowuff. The Germanic tribes,  including  the Angles,  the  Saxons,  and  the Jutes invaded England  from  about A.D. 450  to 600. <span class="hightlight_word" style="margin-right:5px">By the time of the Beowulf poet, Anglo-Saxon society in England was neither primitive nor uncultured </span>
<sup><input type="radio" name="reading_12" title='Click to do the question 12' style="width:12px;height:12px" class="checkbox_model_01_reading_01" id="reading_12_1" value="1"/></sup>
</div>
<div class="clr"></div>
<i class="fa fa-arrow-right"></i> Although the  Beowuff manuscript was written  in about <sub>A.D.</sub> 1000,  it was not 
covered  until  the  seventeenth  century <sup><input type="radio" name="reading_12" title='Click to do the question 12' class="checkbox_model_01_reading_01" id="reading_12_2" value="2"/></sup>. Scholars  do  not know whether 
Beowuff is the sole surviving epic from a flourishing Anglo-Saxon literary period that produced other great epics or whether 
it was <span class="hightlight_word">unique</span>  even in its own  time.<sup><input type="radio" name="reading_12" id="reading_12_3" value="3" title='Click to do the question 12' class="checkbox_model_01_reading_01"/></sup> Many scholars  think that the epic was  probably written sometime  between the late seventh century and  the early ninth century. If they are correct, the original manuscript was probably lost during the ninth-century Viking invasions of Anglia,  in which  the Danes destroyed the Anglo-Saxon monasteries and  their great libraries. However,  other scholars think that the poet's  favorable attitude toward  the Danes must  place the epic's composition after the Viking  invasions and at the  start  of  the  eleventh  century,  when  this  Beowulf manuscript was written.</br> 
<div class="clr"></div>
<i class="fa fa-arrow-right"></i> The identity of the  Beowulf  poet is also uncertain. <sup><input type="radio" name="reading_12" id="reading_12_4" value="4" title='Click to do the question 12' class="checkbox_model_01_reading_01"/></sup> He apparently was a 
Christian who loved the pagan heroic tradition of his ancestors and blended the values of the pagan hero with  
the Christian values of his own country and time. Because he wrote  in  the Anglian  dialect, he probably was either  a monk  in  a 
monastery or  a  poet  in  an  Anglo-Saxon  court  located  north  of  the Thames River</br> 
