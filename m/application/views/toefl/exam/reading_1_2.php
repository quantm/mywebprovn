whales and  seals have a  very  thick  layer of  insulation  fat called  blubber,  just 
under  the skin. Marine mammals swim in water colder than their body core tem-
perature,  and many species spend at  least part of the year in  nearly  freezing 
polar seas. The loss of heat to water occurs 50 to 100 times more rapidly than 
heat loss  to air, and the skin temperature of a marine mammal is close to water 
temperature.  Even so,  the blubber insulation  is  so effective  that marine mammals maintain body core 
tempefatures of about 36-38·C with metabolic rates about the same as <span class="hightlight">those </span> of  land mammals of similar size. The flippers or tail of a whale  or  seal  lack  insulating  blubber, but  countercurrent heat exchangers 
