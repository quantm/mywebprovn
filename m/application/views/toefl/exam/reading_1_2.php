<i class="fa fa-arrow-right"></i> Mammals and  birds generally maintain body temperature within  a  narrow 
range  (36-38°C for most mammals and 39-42°C for most birds) that is usually 
considerably warmer than  the environment. Because heat always flows from a 
warm  object to cooler  surroundings,  birds and mammals must counteract the 
constant heat loss. This maintenance of warm body  temperature depends on 
several key adaptations. The most basic mechanism is the high metabolic rate 
of endothermy itself. Endotherms can produce large amounts of metabolic heat 
that  replace  the  flow  of  heat to  the environment, and they can vary heat 
production·to match  changing rates of  heat  loss. 
<span class='hightlight'>Heat production is increased by such muscle activity as moving or shivering</span>.
In some mammals, certain hormones can cause <span class='text_underline'>mitochondria</span> to increase their metabolic activity  and produce 
heat  instead of <span style="text-decoration:underline">ATP</span>. This <b>nonshivering thermogenesis (NST)</b>  takes  place 
throughout the body, but some mammals also have a tissue called brown fat in 
the neck and between  the  shoulders  that  is specialized  for  rapid  heat production. Through shivering and NST, mammals and birds in cold environments can 
increase their metabolic heat production by as much as 5 to 10 times above the 
<span class='hightlight'>minimal</span> levels that occur in warm conditions.</br>
