<i class="fa fa-arrow-right"></i>Through metabolic heat production, insulation, and vascutar adjustments, 
birds and mammals are capable of astooishing  feats of thermoregulation.  For 
example,  small  birds called  chickadees,  which  weigh  only 20 grams,  can 
remain  active and  hold  body temperature nearly constant at 40°C  in  environmental  temperatures as  low as -40"C-as long  as  they have enough food  to supply the  large amount of energy necessary for heat production. 
Many mammals  and birds  live  in  places where  thermoregulation  requires 
cooling  of as well  as warming. For example, when  a marine mammal moves 
into warm  seas,  as many whales  do when  they  reproduce,  excess  metabolic 
heat  is  removed by vasodilation of numerous blood vessels in the outer layer of
the skin.  In hot climates or when vigorous exercise adds large amounts of metabolic heat to the body, 
many  terrestrial mammals and birds may allow body temperature to rise by several degrees, 
which enhances heat loss by increasing the temperature gradient between the body and a warm environment.
