<i class="fa fa-arrow-right"></i>Evaporative cooling  often  plays a  key  role  in dissipating  the body heat. 
If environmental  temperature is above body temperature, animals gain heat from
the environment as well as  from metabolism, and evaporation is the only way to
keep  body  temperature  from  rising  rapidly. Panting  is  important  in  birds and
many mammals. Some birds have a pouch richly supplied with blood vessels in
the floor of the mouth; fluttering the pouch increases evaporation. Pigeons can
use evaporative cooling  to keep body temperature close to 4O°C in air temperatures as high as 60°C,  
as  long as  they have sufficient water. Many terrestrial mammals have sweat glands controlled by the nervouse system. 
Other mechanisms  that promote evaporative cooting  include spreading saliva on body surfaces, 
an adaptation  of some kangaroos and rodents for combating servere heat stress. Some bats use both saliva and urine to enhance 
evaporative cooling
