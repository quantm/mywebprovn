<span><strong><i class="fa fa-hand-o-right"></i>  Học xong loạt bài giảng này bạn sẽ nắm rõ:</strong><span>
<ul>
	<li>8 kiểu câu hỏi trong phần thi TOEFL iBT Listening</li>
	<li>Cách nhận biết mỗi kiểu câu hỏi</li>
	<li>Hướng dẫn cách trả lời cho mỗi kiểu câu hỏi</li>
	<li>Những chiến lược để tăng điểm trong phần thi Listening</li>
</ul>

<span><strong> <i class="fa fa-hand-o-right"></i> Tổng quan</strong></span>

<p style="margin-left:2%">Trong phần thi TOEFL iBT Listening bạn sẽ nghe từ bốn đến sáu bài giảng và hai đến ba đoạn hội thoại. Mỗi bài giảng có sáu câu hỏi kèm theo và mỗi đoạn hội thoại có năm câu hỏi kèm theo. Bạn có tổng cộng từ 60 đến 90 để trả lời tất cả các câu hỏi.</p>

<p style="margin-left:2%">Như vậy, có hai kiểu bài nghe là hội thoại (conversation) và bài giảng (lecture). Cả hai đều dựa trên những bài nói chuyện có thật diễn ra trong các trường đại học ở Mỹ.</p>

<p style="margin-left:2%">Một bài giảng hoặc một đoạn hội thoại thường kéo dài từ ba đến sáu phút, được nói bằng ngôn ngữ chuẩn trong môi trường giảng dạy. Bạn nên ghi chép lại trong khi nghe bài giảng hoặc hội thoại. Những bài nghe này không có mục đích kiểm tra trí nhớ của bạn.</p>

<span><strong> <i class="fa fa-hand-o-right"></i> Hội thoại (Conversations)</strong></span>

<p style="margin-left:2%">Có hai kiểu hội thoại trong đề thi TOEFL là hội thọai trong văn phòng và hội thoại về dịch vụ. Những đoạn hội thoại này thường diễn ra trong các trường đại học ở Bắc Mỹ</p>

<p style="margin-left:2%"> Những đoạn hội thoại trong văn phòng diễn ra ở trong văn phòng của giáo viên. Nội dung của chúng là về các vấn đề học thuật hoặc liên quan tới khóa học nào đó.</p>
<p style="margin-left:2%">
Hội thoại về dịch vụ diễn ra trong các trường đại học nhưng không có nội dung về học thuật. Ví dụ như về việc trả tiền thuê nhà hoặc đăng ký vào một lớp học.
</p>


<span><strong>II. Mô tả</strong></span>
<div class="clr"></div>
<span style="margin-left:15px"> Phần thi Reading có thời gian làm bài từ 60-100 phút, bao gồm 3-5 bài đọc. Mỗi bài đọc dài khoảng 700 từ và có 12-14 câu hỏi.</span>
<div class="clr" style="height:10px"></div>
<span><strong> <i class="fa fa-hand-o-right"></i> Các bài đọc</strong></span>
<div class="clr"></div>
<span>Bài thi TOEFL iBT sử dụng các bài đọc từ những giáo trình ở trình độ đại học, nói về một chủ đề hoặc quy định nào đó. Các bài đọc bao gồm một loạt những chủ đề khác nhau, nhưng bạn không cần lo lắng nếu không quen với một chủ đề nào đó, vì bài đọc đã bao gồm tất cả những thông tin cần thiết để trả lời các câu hỏi.</span>
<div class="clr" style="height:10px"></div>
<i class="fa fa-square"></i> Tất cả bài đọc đều nằm trong ba loại cơ bản sau:</p>
<ul style="margin-top:-10px">
	<li>Giải thích</li>
	<li>Lập luận</li>
	<li>Lịch sử</li>
</ul>

<span>Đôi khi bài đọc trình bày các thông tin về một chủ đề theo nhiều quan điểm và bạn nên lưu ý điều này trong khi đọc. Bạn thường được hỏi ít nhất một câu để chứng tỏ rằng bạn hiểu cách tổ chức (các ý) trong bài đọc. Những cách tổ chức phổ biến là:</span>

<ul>
	<li>Phân loại</li>
	<li>So sánh / đối chiếu sự tương phản</li>
	<li>Nguyên nhân / kết quả</li>
	<li>Vấn đề / giải pháp</li>
</ul>

<p>Bài đọc sẽ nằm ở bên phải, còn câu hỏi nằm ở bên trái màn hình.</p>

<p><img alt="" src="/images/minh_hoa_reading_screen.jpg" style="height:543px; width:733px"></p>

<span><i class="fa fa-hand-o-right"></i><strong> Định dạng của câu hỏi</strong></span>

<p>Có ba định dạng câu hỏi trong phần thi Reading:</p>
<ul style="margin-top:-10px">
	<li>Câu hỏi có bốn lựa chọn, trong đó có một câu trả lời đúng (định dạng trắc nghiệm truyền thống)</li>
	<li>Câu hỏi có bốn lựa chọn, trong đó có một câu trả lời đúng và yêu cầu thí sinh nhét một câu vào bài đọc tại vị trí phù hợp nhất.</li>
	<li>Câu hỏi “đọc để học” (“reading to learn” questions) có nhiều hơn bốn lựa chọn và có thể có nhiều hơn một câu trả lời đúng. Những câu hỏi này kiểm tra khả năng nhận ra được cách bài đọc tổ chức như thế nào và hiểu được mối quan hệ giữa các sự việc và ý tưởng ở các phần khác nhau trong bài đọc.</li>
</ul>

<span><strong>III. Lời khuyên chung</strong></span>
<div class="clr"></div>
<span>Đọc thường xuyên, đặc biệt là những giáo trình đại học hoặc những tài liệu học thuật khác viết về những chủ đề khác nhau, ví dụ khoa học tự nhiên, khoa học xã hội, nghệ thuật, kinh doanh… Bạn có thể tìm thấy rất nhiều tài liệu học thuật trên Internet hoặc các báo, tạp chí.</span>
<div class='clr'></div>
<p></p>
<p><i class="fa fa-hand-o-right"></i> <strong>Đọc để tìm thông tin</strong></p>
<ul style="margin-top:-10px">
	<li>Dò tìm trong bài đọc những dữ liệu chủ chốt (ngày tháng, con số, thuật ngữ..)</li>
	<li>Luyện đọc thường xuyên để tăng tốc độ đọc và sự trôi chảy</li>
</ul>

<p><i class="fa fa-hand-o-right"></i> <strong>Đọc để hiểu cơ bản</strong></p>
<ul style="margin-top:-10px">
	<li>Tăng vốn từ vựng</li>
	<li>Tập cách đọc lướt nhanh (quickly skimming) qua bài đọc để có ý niệm chung về đại ý của bài, thay vì đọc cẩn thận từng câu từng chữ</li>
	<li>Phát triển khả năng đọc lướt nhanh và tìm ra những điểm chính</li>
	<li>Sau khi đọc lướt bài đọc thì đọc lại nó một lần nữa cẩn thận hơn và viết ra ý chính, những dữ liệu quan trọng</li>
	<li>Chọn vài từ không quen trong bài đọc và tập đoán nghĩa của nó theo ngữ cảnh (những câu văn xung quanh). Sau đó tra từ điển để đối chiếu.</li>
	<li>Gạch dưới các đại từ (<em>he, him, they, them, </em>v.v,…) và chỉ ra nó đang nói đến danh từ nào trong bài đọc.</li>
	<li>Tập suy luận và rút ra kết luận từ những gì được ngụ ý ở trong bài đọc.</li>
</ul>

<p><i class="fa fa-hand-o-right"></i> <strong>Đọc để học (Reading to learn)</strong></p>
<ul style="margin-top:-10px">
	<li>Xác định kiểu viết của bài đọc (viết theo kiểu nguyên nhân / kết quả, so sánh / tương phản, vấn đề / giải pháp; hay mô tả / tường thuật)</li>
	<li>Xem xét cách tổ chức thông tin của bài đọc</li>
</ul>

<span><strong>IV. Các kiểu câu hỏi trong phần thi Reading</strong></span>
<p>Tất cả các câu hỏi đều nhằm kiểm tra kỹ năng tìm thông tin cơ bản, kỹ năng suy luận, và kỹ năng học qua việc đọc (reading to learn). Chúng có thể được chia thành 10 kiểu câu hỏi gắn với từng loại kỹ năng như bảng sau:</p>


<p><i class="fa fa-hand-o-right"></i> <strong>Kỹ năng tìm thông tin cơ bản và Kỹ năng suy luận (11-13 câu hỏi trên mỗi bài đọc)</strong></p>

<p>1. Câu hỏi về thông tin căn cứ theo sự thực (Factual Information) (3-6 câu hỏi trên mỗi bài)</p>

<p>2. Câu hỏi về thông tin không có căn cứ (Negative Factual Information) (0-2 câu hỏi trên mỗi bài)</p>

<p>3. Câu hỏi suy luận (Inference questions) (0-2 câu hỏi trên mỗi bài)</p>

<p>4. Câu hỏi về ý định (0-2 câu hỏi trên mỗi bài)</p>

<p>5. Câu hỏi về từ vựng (3-5 câu hỏi trên mỗi bài)</p>

<p>6. Câu hỏi tham chiếu (0-2 câu hỏi trên mỗi bài)</p>

<p>7. Câu hỏi về giản lược câu (0-1 câu hỏi trên mỗi bài)</p>

<p>8. Câu hỏi yêu cầu nhét câu vào đoạn văn (0-1 câu hỏi trên mỗi bài)</p>

<p><i class="fa fa-hand-o-right"></i> <strong>Kỹ năng Đọc để học (1 câu trên mỗi bài)</strong></p>

<p>9. Tóm tắt</p>

