<div class="reading_section_overview">
	READING  SECTION 
The Reading  section  tests your ability  to understand  reading  passages  like  those  in  college 
textbooks.. The passages are  about 700 words in  length. 
This is the short format for the Reading section. On  the short format, you will  respond to three 
passages. After each passage, you will answer 12- 14 questions about it. 
Most questions are worth  1 point,  but  the  last  question  in each passage  is worth more than 
1 point 
<div class='clr'></div>
You will have 60 minutes to  read all of the passages and answer the questions. You may take 
notes while you read, but notes are not graded. You may use your notes to answer the ques-
tions. Some  passages may  include a word  or phrase  that  is underlined  in  blue. Click on  the 
word or phrase to see a glossary definition or explanation. 
<div class='clr'></div>
Choose the best answer  for multiple-choice question. Follow the directions on the page or on 
the screen for computer-assisted questions. Click on <b>Next</b> to go to the next question. Click on 
<b>Back</b> to  return  to  the previous question. You may  return  to previous questions  for all  of  the 
passages  in  the  same reading part , but  after  you  go  to the  next part,  you  will  not be able  to 
return  to passages in a previous part. Be sure that you have answered all of  the questions for 
the passages in each part before you click on <b>Next</b> at the end of  the passage  to move to the 
next part. 
You can click on <b>Review</b> to see a chart of  the questions you have answered and the questions 
you have not answered in each part. From this screen, you can return to  the question you want 
to answer in the part that is open. 
A clock on the screen will show you how much time you have to complete the Reading section. 
