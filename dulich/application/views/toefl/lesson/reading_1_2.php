	<p><strong>Loại 1: Câu hỏi về thông tin có thực (Factual Information)</strong></p>
<p>Những câu hỏi này yêu cầu bạn chỉ ra những thông tin có thực đã được nói rõ ràng ở trong bài đọc. Câu hỏi về thông tin có thực tập trung vào những sự việc, chi tiết, định nghĩa mà tác giả trình bày. Chúng yêu cầu bạn đi tìm những thông tin cụ thể được đề cập đến trong một phần của bài đọc, chứ không hỏi về những vấn đề chung của toàn bài. Thông thường các thông tin đó nằm trong một hoặc hai câu văn</p>
<span><strong><i class="fa fa-hand-o-right"></i> Làm thế nào để nhận ra những câu hỏi về thông tin có thực ?</strong><span>
<p>Câu hỏi về thông tin có thực thường được diễn đạt theo một trong những cách sau:</p>
<ul>
	<li>Theo đoạn văn, điều gì sau đây là đúng về X? (According to the paragraph, which of the following is true of X?)</li>
	<li>Lời miêu tả về X đề cập đến cái nào trong những cái sau đây? (The author’s description of X mentions which of the following?)</li>
	<li>Theo đoạn văn, X xảy ra là vì … (According to the paragraph, X occurred because . . .)</li>
	<li>Theo đoạn văn, X làm Y là vì … (According to the paragraph, X did Y because . . .)</li>
	<li>Theo đoạn văn, tại sao X làm Y? (According to the paragraph, why did X do Y?)</li>
</ul>

<span><strong> <i class="fa fa-hand-o-right"></i> Gợi ý cách trả lời cho câu hỏi về thông tin có thực</strong></span>
<ul>
	<li>Bạn cần quay lại bài đọc để biết chính xác những gì nói về vấn đề trong câu hỏi, bởi vì câu hỏi hỏi về một chi tiết và bạn sẽ không nhớ nổi sau lần đọc đầu tiên</li>
	<li>Loại bỏ những lựa chọn trình bày thông tin mâu thuẫn</li>
	<li>Đừng chọn một câu trả lời chỉ vì nó được đề cập đến trong đoạn văn, mà lựa chọn của bạn cần phải trả lời câu hỏi cụ thể đang được hỏi</li>
</ul>

<span><strong> <i class="fa fa-hand-o-right"></i> Ví dụ:</strong></span>
<p><strong>ĐOẠN TRÍCH</strong>: “…Sculptures must, for example, be stable, which requires an understanding of the properties of mass, weight distribution, and stress. Paintings must have rigid stretchers so that the canvas will be taut, and the paint must not deteriorate, crack, or discolor. These are problems that must be overcome by the artist because they tend to intrude upon his or her conception of the work. For example, in the early Italian Renaissance, bronze statues of horses with a raised foreleg usually had a cannonball under that hoof. This was done because the cannonball was needed to support the weight of the leg. In other words, the demands of the laws of physics, not the sculptor’s aesthetic intentions, placed the ball there. That this device was a necessary structural compromise is clear from the fact that the cannonball quickly disappeared when sculptors learned how to strengthen the internal structure of a statue with iron braces (iron being much stronger than bronze) . . . “</p>

<p>According to paragraph 2, sculptors in the Italian Renaissance stopped using cannonballs in bronze statues of horses because</p>

<p>(A) they began using a material that made the statues weigh less</p>

<p>(B) they found a way to strengthen the statues internally</p>

<p>(C) the aesthetic tastes of the public had changed over time</p>

<p>(D) the cannonballs added too much weight to the statues</p>


<span><strong> <i class="fa fa-hand-o-right"></i> Giải thích:</strong></span>
<p>Câu hỏi bảo bạn đi tìm câu trả lời trong đoạn văn số 2. Bạn không cần phải đọc hết toàn bộ bài đọc để tìm thông tin liên quan.</p>

<p>Lựa chọn A nói rằng các nhà điêu khắc dừng việc đưa các quả đạn pháo (cannonballs) vào dưới cái chân giơ lên (raised legs) của con ngựa bởi vì họ đã biết cách làm cho bức tượng nhẹ hơn (và không cần phải chống đỡ cho cái chân). Nhưng đoạn văn không hề đề cập đến việc làm cho bức tượng nhẹ hơn, mà nó nói rằng các nhà điêu khắc đã học được cách chống đỡ sức nặng tốt hơn.</p>

<p>Lựa chọn (C) nói rằng thị hiếu của công chúng đã thay đổi theo thời gian (và do đó không cần đưa những quả đạn pháo vào trong cấu trúc). Thông tin này không có thực và mâu thuẫn trong đoạn văn. Lựa chọn (D) nói rằng các quả đạn pháo tăng thêm quá nhiều sức nặng cho bức tượng; và thông tin này cũng là không có thực trong đoạn văn.</p>

<p>Lựa chọn (B) chỉ ra đúng lý do mà đoạn văn nêu ra: các nhà điêu khắc đã phát triển một cách để làm kiên cố (strengthen) bức tượng từ bên trong, khiến cho các quả đạn pháo là không còn cần thiết.</p>

<p>Bạn thấy đó, chỉ cần tìm thông tin trong một đoạn văn và phân tích đối chiếu như trên, ta đã tìm được câu trả lời đúng là (B).</p>



<ul>
	<li>Nhận ra được sự sắp xếp và mục đích của một bài viết</li>
	<li>Hiểu được mối quan hệ giữa các ý</li>
	<li>Sắp xếp thông tin thành một sơ đồ hoặc bản tóm tắt để nhắc lại các ý chính và chi tiết quan trọng</li>
	<li>Suy luận xem các ý trong bài viết kết nối với nhau như thế nào</li>
</ul>

<span><strong>Loại 2: Câu hỏi về thông tin không có căn cứ (Negative Factual Information)</strong></span>

<p>Những câu hỏi này yêu cầu bạn xác nhận thông tin nào là đúng và thông tin nào KHÔNG đúng hoặc không có trong bài đọc, dựa trên những thông tin đã được phát biểu rõ ràng trong bài. Để trả lời loại câu hỏi này, đầu tiên bạn cần tìm nơi có thông tin liên quan. Sau đó xác minh rằng ba trong bốn lựa chọn là đúng và một lựa chọn còn lại là sai. Nhớ rằng đối với loại câu hỏi này, câu trả lời đúng là cái mà mang nội dung KHÔNG đúng.</p>

<p><strong> <i class="fa fa-hand-o-right"></i>Làm thế nào để nhận ra những câu hỏi về thông tin không có thực</strong></p>

<p>Bạn có thể nhận ra chúng bằng những từ “NOT” hoặc “EXCEPT” được in hoa trong câu hỏi.</p>


<p>- Theo bài đọc, trong số những điều sau đây điều nào là KHÔNG đúng về X ? (According to the passage, which of the following is NOT true of X?)</p>

<p>- Sự miêu tả về X đề cập tới tất cả những điều sau TRỪ RA (The author’s description of X mentions all of the following EXCEPT)</p>

<p><strong><i class="fa fa-hand-o-right"></i>Gợi ý cách trả lời cho câu hỏi về thông tin không có thực</strong></p>

<p>- Một câu hỏi này thường yêu cầu bạn kiểm tra ở nhiều đoạn văn hơn so với câu hỏi về thông tin có thực. Ba lựa chọn có thể được đề cập đến ở một hoặc vài đoạn văn.</p>

<p>- Trong câu hỏi về thông tin không có thực, câu trả lời đúng sẽ mâu thuẫn với một hoặc nhiều phát biểu trong bài đọc, hoặc không hề được nêu ra ở trong bài.</p>

<p>- Sau khi trả lời xong thì kiểm tra lại câu trả lời xem có làm đúng theo yêu cầu của câu hỏi không</p>

<span><strong> <i class="fa fa-hand-o-right"></i> Ví dụ :</strong></span>

<p><strong>ĐOẠN TRÍCH: </strong>“The United States in the 1800’s was full of practical, hardworking people who did not consider the arts—from theater to painting—useful occupations. In addition, the public’s attitude that European art was better than American art both discouraged and infuriated American artists. In the early 1900’s there was a strong feeling among artists that the United States was long overdue in developing art that did not reproduce European traditions. Everybody agreed that the heart and soul of the new country should be reflected in its art. But opinions differed about what this art would be like and how it would develop.”</p>

<p>According to paragraph 1, all of the following were true of American art in the late 1800’s and early 1900’s EXCEPT:</p>

<p>(A) Most Americans thought art was unimportant.</p>

<p>(B) American art generally copied European styles and traditions.</p>

<p>(C) Most Americans considered American art inferior to European art.</p>

<p>(D) American art was very popular with European audiences.</p>


<span><strong> <i class="fa fa-hand-o-right"></i> Giải thích:</strong></span>
<p>Đối với những câu hỏi về thông tin không có thực, đôi khi cần thiết phải kiểm tra toàn bộ bài đọc để chắc chắn một lựa chọn không được nói đến. Tuy nhiên trong ví dụ này câu hỏi được giới hạn trong một đoạn văn vì vậy câu trả lời của bạn chỉ dựa trên thông tin trong đoạn văn đó. Lựa chọn (A) chỉ diễn đạt lại câu đầu tiên của đoạn văn: bởi vì hầu hết người Mỹ không nghĩ rằng nghệ thuật là một nghề hữu ích, họ coi nó là không quan trọng. Lựa chọn (B) cũng nói cùng một ý của câu văn thứ ba: “. . . the United States was long overdue in developing art that did not reproduce European traditions” (Hoa Kỳ đã quá chậm trễ trong việc phát triển một nền nghệ thuật trong đó không sao chép những truyền thống của châu Âu), có nghĩa là cho tới thời điểm đó trong lịch sử, nền nghệ thuật Mỹ đã luôn sao chép những truyền thống của châu Âu. Lựa chọn C là đúng vì trong đoạn văn có nói “the public’s attitude that European art was better than American art” (thái độ của công chúng là nghệ thuật của châu Âu hay hơn nghệ thuật của Mỹ). Còn lựa chọn (D) thì không hề được nói đến ở bất kỳ đâu trong đoạn văn, nên câu trả lời đúng ở đây là (D).</p>

<span><strong>Kiểu 3: Câu hỏi đòi hỏi suy luận (Inference Questions)</strong></span>
<p>Những câu hỏi này kiểm tra khả năng hiểu một sự lập luận hoặc một ý tưởng được ngụ ý và không được viết ra trong bài đọc. Ví dụ nếu một kết quả được viết ra ở trong bài đọc thì câu hỏi sẽ hỏi về nguyên nhân của nó. Nếu trong bài có sự so sánh thì câu hỏi sẽ hỏi về cơ sở của sự so sánh đó. Bạn cần nghĩ về những sự ngụ ý logic trong lời văn.</p>
<i class="fa fa-hand-o-right"></i> Làm thế nào để nhận ra những câu hỏi suy luận

<p>Những câu hỏi này sẽ có những từ như <em>infer (suy ra), suggest (ám chỉ), </em>or <em>imply</em> <em>(gợi ý)</em></p>

<p>- Có thể suy ra điều nào trong số những điều sau về X? (Which of the following can be inferred about X?)</p>

<p>- Tác giả của bài văn ngụ ý rằng X … (The author of the passage implies that X . . .)</p>


<p><strong><i class="fa fa-hand-o-right"></i>Gợi ý cách trả lời cho câu hỏi suy luận</strong></p>

<p>- Chắc chắn rằng câu trả lời của bạn không mâu thuẫn với đại ý của bài đọc.</p>

<p>- Đừng chọn chỉ vì nó có vẻ quan trọng hoặc đúng. Câu trả lời đúng phải được suy luận ra từ bài văn.</p>

<p>- Bạn hoàn toàn có thể bảo vệ lựa chọn của mình bằng cách chỉ ra những thông tin được viết rõ ràng trong bài văn và dẫn đến sự kết luận đó.</p>

<span><strong> <i class="fa fa-hand-o-right"></i> Ví dụ :</strong></span>

<p><strong>ĐOẠN TRÍCH: </strong>“. . . The nineteenth century brought with it a burst of new discoveries and inventions that revolutionized the candle industry and made lighting available to all. In the early-to-mid-nineteenth century, a process was developed to refine tallow (fat from animals) with alkali and sulfuric acid. The result was a product called stearin. Stearin is harder and burns longer than unrefined tallow. This breakthrough meant that it was possible to make tallow candles that would not produce the usual smoke and rancid odor. Stearins were also derived from palm oils, so vegetable waxes as well as animal fats could be used to make candles . . . “</p>

<p>Which of the following can be inferred from paragraph 1 about candles before the nineteenth century?</p>

<p>(A) They did not smoke when they were burned.</p>

<p>(B) They produced a pleasant odor as they burned.</p>

<p>(C) They were not available to all.</p>

<p>(D) They contained sulfuric acid.</p>

<span><strong> <i class="fa fa-hand-o-right"></i> Giải thích:</strong></span>

<p>Câu đầu tiên có nói “new discoveries and inventions” khiến cho “lighting available to all.” Đây là phát kiến mới do đó ta có thể suy luận vào trước đó (trước thế ký 19) thì nến (candles) không có sẵn cho mọi người. Vậy lựa chọn (C) là suy luận đúng. Lựa chọn (A) và (B) bị loại bỏ vì chúng mâu thuẫn với những dữ kiện trong bài đọc (“the usual smoke” and “rancid odor”). Lựa chọn (D) bị loại bỏ vì cho đến thế kỉ 19 thì sulfuric acid mới được dùng để sản xuất stearin, chứ không phải trước đó.</p>

<span><strong>Câu hỏi về ý định</strong></span>
<p>Trong câu hỏi về thông tin có thực, bạn được hỏi là tác giả đã trình bày những thông tin gì. Còn trong câu hỏi về ý định, bạn được hỏi là tại sao tác giả lại trình bày những thông tin đó, vì mục đích gì? Bạn cần phải hiểu được mục đích của một lời phát biểu hoặc một đoạn văn.</p>

<p><strong><i class="fa fa-hand-o-right"></i>Làm thế nào để nhận ra những câu hỏi về ý định</strong></p>

<p>- Chúng thường được diễn đạt theo kiểu như sau:</p>

<p>- Tác giả thảo luận về X trong đoạn văn số 2 là để … (The author discusses X in paragraph 2 in order to . . .)</p>

<p>- Tại sao tác giả đề cập đến X? (Why does the author mention X?)</p>

<p>- Tác giả dùng X làm ví dụ của … (The author uses X as an example of . . .)</p>

<p><strong> <i class="fa fa-hand-o-right"></i> Gợi ý cách trả lời cho câu hỏi về ý định</strong></p>

<p>- Bạn cần hiểu nghĩa của những từ được dùng để diễn đạt ý định:</p>

<p>&nbsp;“definition,” “example,” “to illustrate,” “to explain,” “to contrast,” “to refute,” “to note,” “to criticize,”</p>

<p>“function of.”</p>

<p>- Câu hỏi về ý định thường không hỏi về cấu trúc chung của bài đọc mà nó chủ yếu tập trung vào mối liên hệ logic giữa câu và đoạn văn.</p>

<p><strong> <i class="fa fa-hand-o-right"></i> Ví dụ</strong></p>

<p><strong>ĐOẠN TRÍCH: </strong>“. . . Sensitivity to physical laws is thus an important consideration for the maker of applied-art objects. It is often taken for granted that this is also true for the maker of fine-art objects. This assumption misses a significant difference between the two disciplines. Fine-art objects are not constrained by the laws of physics in the same way that applied-art objects are. Because their primary purpose is not functional, they are only limited in terms of the materials used to make them. Sculptures must, for example, be stable, which requires an understanding of the properties of mass, weight distribution, and stress. Paintings must have rigid stretchers so that the canvas will be taut, and the paint must not deteriorate, crack, or discolor. These are problems that must be overcome by the artist because they tend to intrude upon his or her conception of the work. For example, in the early Italian Renaissance, bronze statues of horses with a raised foreleg usually had a cannonball under that hoof. This was done because the cannonball was needed to support the weight of the leg . . . “</p>

<p>Why does the author discuss the bronze statues of horses created by artists in the early Italian Renaissance?</p>

<p>(A) To provide an example of a problem related to the laws of physics that a fine artist must overcome</p>

<p>(B) To argue that fine artists are unconcerned with the laws of physics</p>

<p>(C) To contrast the relative sophistication of modern artists in solving problems related to the laws of physics</p>

<p>(D) To note an exceptional piece of art constructed without the aid of technology</p>

<p><strong><i class="fa fa-hand-o-right"></i>Giải thích</strong></p>

<p>Chúng ta thấy rằng tác giả đưa ra ví dụ về bức tượng ngựa bằng đồng để minh họa cho vấn đề mà tác giả thảo luận từ đầu đoạn văn. Tác giả thảo luận rằng những quy luật vật lý có những ảnh hưởng khác nhau lên mỹ thuật và nghệ thuật ứng dụng hoặc thủ công mỹ nghệ. Nghệ sỹ mỹ thuật không tạo ra những vật dụng hàng ngày nên anh ta không bị hạn chế nhiều như nghệ thuật ứng dụng. Nhưng dù vậy thì tác phẩm nghệ thuật vẫn phải được tạo ra từ một số vật liệu do đó người nghệ sỹ vẫn cần quan tâm đến những tính chất vật lý của vật liệu đó. Vì thế lựa chọn (B) nói người nghệ sỹ không quan tâm đến các quy luật vật lý là sai. Ví dụ về tượng ngựa đồng cho thấy nghệ sỹ phải dùng những vật chống đỡ cho cái chân giơ lên của con ngựa, tức là xử lý vấn đề về trọng lượng, đúng với ý của lựa chọn (A). Vậy lựa chọn (A) là đúng.</p>


